    private void registarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registarActionPerformed
        // TODO add your handling code here:
        String nome = this.nomeRegistar.getText();
        String email = this.emailRegistar.getText();
        String password = this.passwordRegistar.getText();
        if (nome.equals("") || email.equals("") || password.equals("")){
            this.mensagemExcecao.setText("Tem de preencher os campos todos referentes ao registo!");
            this.dialogoExcecoes.setVisible(true);
            return;
        }
        try{
            f.registarMorador(nome, email, password);
            this.mensagemSucesso.setText("Morador Registado!");
            this.dialogoSucesso.setVisible(true);
        }
        catch (EmailInvalidoException e){
            this.mensagemExcecao.setText(e.getMessage());
            this.dialogoExcecoes.setVisible(true);
        }
    }//GEN-LAST:event_registarActionPerformed

