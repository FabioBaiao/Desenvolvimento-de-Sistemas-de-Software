    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        String email = this.emailLogin.getText();
        String password = this.passwordLogin.getText();
        try{
            f.autenticarMorador(email, password);
            this.dispose();
            MainWindow mw = new MainWindow(f);
            mw.setVisible(true);
        }
        catch (EmailInvalidoException | PasswordErradaException e){
            this.mensagemExcecao.setText(e.getMessage());
            this.dialogoExcecoes.setVisible(true);
        }
    }//GEN-LAST:event_loginActionPerformed

