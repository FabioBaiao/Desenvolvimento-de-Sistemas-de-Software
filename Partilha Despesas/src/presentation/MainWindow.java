    private void okAdicionarDespesaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okAdicionarDespesaActionPerformed
        // TODO add your handling code here:
        String descricao = this.descricaoAdicionarDespesa.getText();
        if (descricao.equals("")){
            this.mensagemExcecao.setText("Descricao invalida!");
            this.dialogoExcecao.setVisible(true);
            return;
        }
        String dataLimite = this.diaAdicionarDespesa.getSelectedItem() + "-" + this.mesAdicionarDespesa.getSelectedItem() + "-" + this.anoAdicionarDespesa.getSelectedItem();
        double valor = 0;
        try{
            valor = Double.parseDouble(this.valorAdicionarDespesa.getText());
        }
        catch(NumberFormatException e){
            this.mensagemExcecao.setText("Valor invalido!");
            this.dialogoExcecao.setVisible(true);
            return;
        }
        String tipo = "";
        if (this.recorrenteAdicionarDespesa.isSelected()){
            tipo = "Recorrente";
        }
        if (this.extraordinariaAdicionarDespesa.isSelected()){
            tipo = "Extraordinaria";
        }
        if (tipo.equals("")){
            this.mensagemExcecao.setText("Tem de selecionar um tipo!");
            this.dialogoExcecao.setVisible(true);
            return;
        }
        try{
            if (this.equitativaAdicionarDespesa.isSelected()){
                String m = this.moradoresAdicionarDespesa.getText();
                List<String> moradores = new ArrayList<>();
                String[] strs = m.split("\n");
                for (String s : strs){
                    s = s.trim();
                    if (!s.equals(""))
                        moradores.add(s);
                }
                f.adicionarDespesa(descricao, valor, moradores, tipo, dataLimite);
                this.adicionarDespesaJanela.dispose();
                apresentarDespesas();
            }
            else if (this.percentualAdicionarDespesa.isSelected()){
                String m = this.moradoresAdicionarDespesa.getText();
                List<String> moradores = new ArrayList<>();
                String[] strs = m.split("\n");
                for (String s : strs){
                    s = s.trim();
                    if (!s.equals(""))
                        moradores.add(s);
                }
                m = this.percentagensAdicionarDespesa.getText();
                List<Double> percentagens = new ArrayList<>();
                strs = m.split("\n");
                for (String s : strs){
                    s = s.trim();
                    if (!s.equals(""))
                        percentagens.add(Double.parseDouble(s));
                }
                f.adicionarDespesa(descricao, valor, moradores, percentagens, tipo, dataLimite);
                this.adicionarDespesaJanela.dispose();
                apresentarDespesas();
            }
            else{
                this.mensagemExcecao.setText("Tem de selecionar divisao!");
                this.dialogoExcecao.setVisible(true);
            }
        }
        catch (EmailInvalidoException | ValorInvalidoException | PercentagemInvalidaException | DespesaInvalidaException e){
            this.mensagemExcecao.setText(e.getMessage());
            this.dialogoExcecao.setVisible(true);
        }
        
    }//GEN-LAST:event_okAdicionarDespesaActionPerformed

