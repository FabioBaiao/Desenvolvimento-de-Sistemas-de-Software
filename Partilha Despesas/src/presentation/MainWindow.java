    private void okEditarDespesaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okEditarDespesaActionPerformed
        // TODO add your handling code here:
        int i = this.listaDespesas.getSelectedIndex();
        String s = this.despesas.get(i);
        String[] ss = s.split(" ");
        int id = Integer.parseInt(ss[0]);
        try{
            String descricao = this.descricaoEditarDespesa.getText();
            if (!descricao.equals("")){
                f.editarDescricao(id, descricao);
            }
            String dataLimite = this.diaEditarDespesa.getSelectedItem() + "-" + this.mesEditarDespesa.getSelectedItem() + "-" + this.anoEditarDespesa.getSelectedItem();
            f.editarData(id, dataLimite);
            s = this.valorEditarDespesa.getText();
            if (!s.equals("")){
                try{
                    double valor = Double.parseDouble(s);
                    f.editarValor(id, valor);
                }
                catch (NumberFormatException e){
                    this.mensagemExcecao.setText("Valor invalido");
                    this.dialogoExcecao.setVisible(true);
                }
            }
            apresentarDespesas();
            this.editarDespesaJanela.dispose();
        }
        catch (DespesaInvalidaException | ValorInvalidoException e){
            this.mensagemExcecao.setText(e.getMessage());
            this.dialogoExcecao.setVisible(true);
        }
    }//GEN-LAST:event_okEditarDespesaActionPerformed

