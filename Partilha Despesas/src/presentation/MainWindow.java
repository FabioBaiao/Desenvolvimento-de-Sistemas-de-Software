    private void okEfetuarPagamentoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okEfetuarPagamentoActionPerformed
        // TODO add your handling code here:
        int i = this.listaDespesasIndividuais.getSelectedIndex();
        String s = this.despesasIndividuais.get(i);
        String[] ss = s.split(" ");
        int id = Integer.parseInt(ss[0]);
        
        s = this.valorEfetuarPagamento.getText();
        double valor;
        try{
            valor = Double.parseDouble(s);
        }
        catch(NumberFormatException e){
            this.mensagemExcecao.setText("Valor invalido!");
            this.dialogoExcecao.setVisible(true);
            return;
        }
        String data = this.diaEfetuarPagamento.getSelectedItem() + "-" + this.mesEfetuarPagamento.getSelectedItem() + "-" + this.anoEfetuarPagamento.getSelectedItem();
        try{
            if (this.numerarioEfetuarPagamento.isSelected()){
                f.efetuarPagamento(id, valor, "Numerario", data);
                apresentarDespesasIndividuais();
                apresentarPagamentos();
                this.efetuarPagamentoJanela.dispose();
                return;
            }
            if (this.transferenciaEfetuarPagamento.isSelected()){
                f.efetuarPagamento(id, valor, "Transferencia", data);
                apresentarDespesasIndividuais();
                apresentarPagamentos();
                this.efetuarPagamentoJanela.dispose();
                return;
            }
            this.mensagemExcecao.setText("Tem de selecionar um metodo!");
            this.dialogoExcecao.setVisible(true);
        }
        catch (ValorInvalidoException | DespesaInvalidaException e){
            this.mensagemExcecao.setText(e.getMessage());
            this.dialogoExcecao.setVisible(true);
        }
    }//GEN-LAST:event_okEfetuarPagamentoActionPerformed

